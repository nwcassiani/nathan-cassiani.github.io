////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  if (num1 < num2) {
    return num1;
  } else if (num1 > num2) {
    return num2; 
  } else {
    return num1, num2;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  // if num is zero return true
  if (num == 0) {
    return true;
  } else if (num == 1) {
    return false;
  } else if (num < 0) {
    return isEven(-num);
  } else {
    return isEven(num - 2);
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
    // init sum variable
    let sum = 0;
    // takes a string as an argument and returns how many chars are in string
    // loop over string and compare [i] with char
    for (let i = 0; i < string.length; i++) {
      if (string[i] === char) {
        sum += 1;
      }
    }
    // return sum
    return sum;

}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  // init sum variable
  let sum = 0;
  // takes a string as an argument and returns how many upper case B's are in string
  // loop over string and compare [i] with letter uppercase B
  for (let i = 0; i < string.length; i++) {
    if (string[i] === 'B') {
      sum += 1;
    }
  }
  // return sum
  return sum;
}

