////////////////////////////////////////////////////////////////////////////////
// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step = 1) {
  // outputs an array from start to end, inclusive
  // output array
  let output = [];
  // if start and end are equal return empty array
  if (start == end) {
    return output;
  }
  // if step is positive
  if (step > 0){
    for (let i = start; i <= end; i += step){
      output.push(i);
  } 
  // if step is negative
  if (step < 0){
    for (let i = start;i >= end; i += step){
      output.push(i);
    }
  }
}
  return output;

}

////////////////////////////////////////////////////////////////////////////////
// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(array) {
  let total = 0;
  for (let i = 0; i < array.length; i++){
    total += array[i];
  }
  return total;
}

////////////////////////////////////////////////////////////////////////////////
// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(array) {
  // output array
  let output = [];
  // for loop 
  for (let i = array.length - 1; i >= 0; i--){
    // push to output
    output.push(array[i]);
  }
  return output;
}

////////////////////////////////////////////////////////////////////////////////
// reverseArrayInPlace /////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArrayInPlace(array) {
  // loop over array
  for (var i = array.length - 1; i >= 0; i--){
    var reversed = array[i];
  }
  array = reversed;
  return array;

}

////////////////////////////////////////////////////////////////////////////////
// arrayToList /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function arrayToList(array) {
  let rest = null;
  // loop over array in reverse
  for (let i = array.length - 1; i >= 0; i--){
    rest = {value: array[i], rest: rest};
  }
  // return rest
  return rest;


}

////////////////////////////////////////////////////////////////////////////////
// listToArray /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function listToArray(list, array=[]) {
  // base 
  if(list.rest === null){
    array.push(list.value);
    return array;
  }
  // recursion
  array.push(list.value);

  return listToArray(list.rest, array);
}

////////////////////////////////////////////////////////////////////////////////
// prepend /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function prepend(value, list) {
  // returns a new list that adds the element to the front of the input list
  return {value, rest: list};

}

