// loop ////////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function loop(start, test, update, body) {
  // using for loop 
  let value = start;
  for (value; test(value); value = update(value)){
    // pass value through body function
    body(value);
  }

}

